package operators_2;

public class ComparisonDemo_4 {
	public static void main(String[] args) {
		int value1 = 1;
		int value2 = 2;

		// change the value1 and value2 properly ...
		if (value1 == value2)
			System.out.println("value1 == value2");
		if (value1 != value2)
			System.out.println("value1 != value2");
		if (value1 > value2)
			System.out.println("value1 > value2");
		if (value1 < value2)
			System.out.println("value1 < value2");
		if (value1 <= value2)
			System.out.println("value1 <= value2");

	}
}
